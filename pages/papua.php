            <h1>Papua Culture</h1>
            
            <p>
                The Papua shield collection at the British Museum showcases the rich artistic and warrior traditions of Papua 
                New Guinea. These shields, often made from wood and painted with striking patterns, were used in battle as well 
                as in ceremonial contexts. The bold geometric and figurative designs not only served aesthetic purposes but also 
                held deep cultural and spiritual significance, symbolizing clan identity and ancestral protection.
            </p>

            <div class="highlight-box">
                <p class="mb-0">
                    <i class="bi bi-shield text-warning me-2"></i>
                    <strong>Cultural Heritage:</strong> Shields were passed down through generations, carrying community legacy and history
                </p>
            </div>

            <p>
                Many Papua shields were decorated with natural pigments such as red ochre, black charcoal, and white clay, 
                creating visually stunning contrasts. The motifs depicted on these shields often included totemic animals, 
                mythological figures, or spirits believed to provide strength and protection in combat. Some shields also 
                featured raised carvings or attachments, adding to their unique appearance and cultural meaning.
            </p>

            <p>
                Beyond warfare, these shields played an important role in ritual performances and social status. They were 
                often passed down through generations, carrying the legacy of their owners and their community's history. 
                Today, the Papua shields in the British Museum offer a glimpse into the artistry and beliefs of the indigenous 
                peoples of Papua New Guinea, preserving a vital part of their heritage for the world to appreciate.
            </p>
